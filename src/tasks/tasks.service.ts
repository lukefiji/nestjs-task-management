  async getTaskById(id: number): Promise<Task> {
    const found = await this.taskRepository.findOne(id);

    if (!found) {
      // Escalates to NestJS beacuse of no try/catch
      throw new NotFoundException(`Task with ID '${id}' not found`);
    }

    return found;
  }

